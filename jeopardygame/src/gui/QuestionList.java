    public QuestionList(ArrayList<JeopardyGame>games,ArrayList<Student>students,ArrayList<Question>questList,Quiz quiz, int gameIndex) {
        initComponents();
        this.games = new ArrayList<>();
        this.students = new ArrayList<>();
        this.questList = new ArrayList<>();
        this.games.addAll(games);
        this.students.addAll(students);
        this.questList.addAll(questList);
        this.gameIndex = gameIndex;
        this.quiz = new Quiz(quiz);
        quiz.setQuestions(questList);
        ListModel model = new ListModel();
        for (Question q : this.questList){
            model.addElement(q.questionToString());
        }
        jList1.setModel(model);
    }
